mammal.combined <- readRDS('../Data/Three_dataset.rds')

uID <- as.character(mammal.combined$DataOrder)
uID[which(uID=="1) Marmoset")] <- "Marm1"
uID[which(uID=="2) Cyno")] <- "Cyno1"
uID[which(uID=="3) Human")] <- "Human1"
mammal.combined$uData <- uID

Idents(mammal.combined) <- paste(mammal.combined$uData,Idents(mammal.combined), sep="_")
AvE <- AverageExpression(mammal.combined)

C <- cor(log2(AvE$integrated[,c('Marm1_Epi_CS3','Marm1_EmDisc_CS5','Marm1_EmDisc_CS6','Marm1_EmDisc_CS7','Marm1_PGC_CS5','Marm1_PGC_CS6','Marm1_PGC_CS7','Marm1_Hyp_CS3','Marm1_VE_CS5','Marm1_VE_CS6','Marm1_VE_CS7','Marm1_SYS_CS5','Marm1_SYS_CS6','Marm1_SYS_CS7','Marm1_ExMes_CS5','Marm1_ExMes_CS6','Marm1_ExMes_CS7','Marm1_Tb_CS3','Marm1_Tb_CS5','Marm1_Tb_CS6','Marm1_Tb_CS7')]+1),log2(AvE$integrated[,c('Cyno1_Epi_CS3','Cyno1_EmDisc_CS5','Cyno1_EmDisc_CS6/7','Cyno1_EmDiscPS_CS6/7','Cyno1_PGC_E50','Cyno1_Hyp_CS3','Cyno1_VE_CS5','Cyno1_SYS_CS6/7','Cyno1_ExMes_CS5','Cyno1_ExMes_CS6/7','Cyno1_Tb_CS3','Cyno1_Tb_CS6/7')]+1),method="pearson")
